conjunto1 = {1,2,3,5,2,4}

print(conjunto1)

conj1 = {1,4,5,2}
conj2 = {4,3,7,5}

print(conj1)
print(conj2)

print("Unio de Conjuntos : ") 
print(conj1.union(conj2))

print("Interseccion de Conjuntos : ") 
print(conj1.intersection(conj2))

print("Deferencia de Conjuntos : ") 
print(conj1.difference(conj2))

print("Diferencia Simetrica de Conjuntos : ") 
print(conj1.symmetric_difference(conj2))

print(conjunto1.add(8))
